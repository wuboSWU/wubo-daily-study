package basic;

public class IntegerToBinary {
    public static void main(String[] args) {
        //int转2进制
        System.out.println(Integer.toBinaryString(12));
        //int转8进制
        System.out.println(Integer.toOctalString(12));
        //int转16进制
        System.out.println(Integer.toHexString(12));
    }
}
